# While loops are similar
n = 0
while n < 5:
    print(n)
    n += 1

print("\n")

# Looping from i = 0 to i = 4
for i in range(5):
    print(i)

print("\n")

# Looping from i = 2 to i = 5
for i in range(2, 6):
    print(i)

print("\n")

# Looping in reverse from i = 5 to i = 2
for i in range(5, 1, -1):
    print(i)
